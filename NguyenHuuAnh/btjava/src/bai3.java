import java.util.Scanner;

public class bai3 {
    public static void main(String[] args) {
        //for
        //1. In dãy số từ 1 -> 100
        System.out.print("Dãy số từ 1 -> 100: ");
        for (int i = 1; i <= 100; i++){
            System.out.print(i+" ");
        }
        System.out.println();

        //2. In ra bảng chữ cái từ A -> Z
        System.out.println("Bảng chữ cái từ A -> Z: ");
        for (char i = 65; i <= 90; i++){
            System.out.print(i+" ");
        }
        System.out.println();
    }
}
